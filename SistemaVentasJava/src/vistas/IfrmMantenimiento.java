    void eliminarCliente(){
        int fila = tablaCliente.getSelectedRow();
        int ids = Integer.parseInt(tablaCliente.getValueAt(fila, 0).toString());
        if(fila==-1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }else{
          cliDao.eliminar(ids);
        }
    }
