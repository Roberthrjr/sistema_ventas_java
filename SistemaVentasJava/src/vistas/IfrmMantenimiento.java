    void modificarVendedor() {
        int fila = tablaVendedor.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una Fila");
        } else {
            int id = Integer.parseInt(modeloVendedor.getValueAt(fila, 0).toString());
            String dni = txtDniVendedor.getText();
            String nom = txtNombreVendedor.getText();            
            String cel= txtCelularVendedor.getText();
            String estado = cboEstadoVendedor.getSelectedItem().toString();            
            ven.setDni(dni);
            ven.setNom(nom);
            ven.setTel(cel);
            ven.setEstado(estado);
            ven.setId(id);
            r=venDao.actualizar(ven);
            if(r==1){
                JOptionPane.showMessageDialog(this, "Vendedor Actualizado con Exito..!!");
            }
            else{
                JOptionPane.showMessageDialog(this, "Error");
            }
            LimpiarTablaVendedor();
            listarVendedor();
            nuevoVendedor();
        }
    }

