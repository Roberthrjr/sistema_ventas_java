    private void btnModificarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarProductoActionPerformed
        int fila = tablaProducto.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una Fila");
        } else {
            int id = Integer.parseInt(tablaProducto.getValueAt(fila, 0).toString());
            String nom = txtNombreProducto.getText();
            double pre = Double.parseDouble(txtPrecioProducto.getText());
            int stock = Integer.parseInt(txtStockProducto.getText());
            String estado = cboEstadoProducto.getSelectedItem().toString();
            pro.setNom(nom);
            pro.setPre(pre);
            pro.setStock(stock);
            pro.setEstado(estado);
            pro.setId(id);
            proDao.actualizar(pro);
            JOptionPane.showMessageDialog(this, "Producto Actualizado con Exito");
            LimpiarTablaProducto();
            listarProducto();
            nuevoProducto();
        }
    }//GEN-LAST:event_btnModificarProductoActionPerformed

